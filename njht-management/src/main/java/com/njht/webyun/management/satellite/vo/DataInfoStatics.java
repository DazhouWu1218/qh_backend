package com.njht.webyun.management.satellite.vo;

public class DataInfoStatics {
    private Double area;
    private Integer satelliteType;
    private String geostr;

    public Double getArea() {
        return area;
    }

    public void setArea(Double area) {
        this.area = area;
    }

    public Integer getSatelliteType() {
        return satelliteType;
    }

    public void setSatelliteType(Integer satelliteType) {
        this.satelliteType = satelliteType;
    }

    public String getGeostr() {
        return geostr;
    }

    public void setGeostr(String geostr) {
        this.geostr = geostr;
    }
}
